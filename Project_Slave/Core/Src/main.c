void JoystickSend() // Sending Joystick's values to Master board
{
	if(adc_value[1] <= 100) {comp = 0;} //U -> UP
	else if(adc_value[1] >= 4000) {comp = 1;} //D -> DOWN
	else if(adc_value[0] >= 4000 ) {comp = 2;} //L -> LEFT
	else if(adc_value[0] <= 100) {comp = 3;} //R -> RIGHT
	else {comp = 4;} // Sending nothing

	//HAL_UART_Transmit(&huart1, &comp, comp, 0xFFFF); // Sending Joystick's values to Master Board
}

